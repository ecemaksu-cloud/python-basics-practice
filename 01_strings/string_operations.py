# Character replacement

word = "Galata"
word = "S" + word[1:]
print(word)

# String slicing

place = "istanbul"
print(place[0::2])
print(place[1::4])

# Splitting a word

team = "GALATASARAY"

word1 = team[0:6]
word2 = team[6:11]

print(word1)
print(word2)

# Reverse string

name = "kaan"
print(name[::-1])

# Type conversion

z = "8.77235641364654"

a = float(z)
b = int(a)

print(a)
print(b)
